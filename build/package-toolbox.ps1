param(
    [string]$Configuration = "Release",
    [string]$PackageVersion = "0.1.1",
    [string]$RuntimeIdentifier = "win-x64",
    [switch]$SkipNpmInstall
)

$ErrorActionPreference = "Stop"

$root = Resolve-Path -LiteralPath (Join-Path $PSScriptRoot "..")
$toolboxProject = Join-Path $root "src\HUnityAutoTranslator.Toolbox\HUnityAutoTranslator.Toolbox.csproj"
$toolboxUiRoot = Join-Path $root "src\HUnityAutoTranslator.Toolbox.Ui"
$toolboxUiBuildRoot = Join-Path $PSScriptRoot ".toolbox-ui-build"
$publishRoot = Join-Path $PSScriptRoot "HUnityAutoTranslator-Toolbox"
$outputZip = Join-Path $PSScriptRoot "HUnityAutoTranslator-Toolbox-$PackageVersion-$RuntimeIdentifier.zip"

function Invoke-CheckedNative([string]$Command, [string[]]$Arguments) {
    & $Command @Arguments
    if ($LASTEXITCODE -ne 0) {
        throw "Command failed with exit code ${LASTEXITCODE}: $Command $($Arguments -join ' ')"
    }
}

function Normalize-Newlines([string]$Value) {
    return ($Value -replace "`r`n", "`n") -replace "`r", "`n"
}

function Read-ToolboxDistAsset([string]$DistRoot, [string]$AssetReference) {
    $assetPath = $AssetReference.TrimStart(".", "/", "\").Replace("/", [System.IO.Path]::DirectorySeparatorChar)
    $fullPath = Join-Path $DistRoot $assetPath
    if (-not (Test-Path -LiteralPath $fullPath)) {
        throw "Referenced toolbox asset not found: $AssetReference"
    }

    return Normalize-Newlines (Get-Content -LiteralPath $fullPath -Raw -Encoding UTF8)
}

function Write-ToolboxHtml([string]$InputHtml, [string]$OutputFile) {
    if (-not (Test-Path -LiteralPath $InputHtml)) {
        throw "Built toolbox HTML not found: $InputHtml"
    }

    $inputPath = Resolve-Path -LiteralPath $InputHtml
    $distRoot = Split-Path -Parent $inputPath
    $html = Normalize-Newlines (Get-Content -LiteralPath $inputPath -Raw -Encoding UTF8)

    if ($html -match '(?i)<(?:script|link|img|source|iframe|audio|video)[^>]+(?:src|href)="https?://') {
        throw "Generated toolbox HTML must not reference remote assets."
    }

    $html = [regex]::Replace(
        $html,
        '<link[^>]+rel="modulepreload"[^>]*>',
        '',
        [System.Text.RegularExpressions.RegexOptions]::IgnoreCase)

    $html = [regex]::Replace(
        $html,
        '<link[^>]+rel="stylesheet"[^>]+href="([^"]+)"[^>]*>',
        {
            param($match)
            $css = Read-ToolboxDistAsset $distRoot $match.Groups[1].Value
            "<style>`n$css`n</style>"
        },
        [System.Text.RegularExpressions.RegexOptions]::IgnoreCase)

    $html = [regex]::Replace(
        $html,
        '<script[^>]+type="module"[^>]+src="([^"]+)"[^>]*></script>',
        {
            param($match)
            $js = Read-ToolboxDistAsset $distRoot $match.Groups[1].Value
            "<script>`n$js`n</script>"
        },
        [System.Text.RegularExpressions.RegexOptions]::IgnoreCase)

    $escaped = $html.Replace('"""', '""""')
    $source = @(
        'namespace HUnityAutoTranslator.Toolbox;'
        ''
        'internal static class ToolboxHtml'
        '{'
        '    // <auto-generated by build/package-toolbox.ps1>'
        '    public const string Document = """'
        $escaped
        '""";'
        '}'
        ''
    ) -join "`n"

    [System.IO.File]::WriteAllText($OutputFile, $source, [System.Text.UTF8Encoding]::new($false))
}

if (Test-Path -LiteralPath $toolboxUiBuildRoot) {
    Remove-Item -LiteralPath $toolboxUiBuildRoot -Recurse -Force
}
New-Item -ItemType Directory -Force -Path $toolboxUiBuildRoot | Out-Null

Get-ChildItem -LiteralPath $toolboxUiRoot -Force |
    Where-Object { $_.Name -notin @("node_modules", "dist") } |
    ForEach-Object {
        Copy-Item -LiteralPath $_.FullName -Destination (Join-Path $toolboxUiBuildRoot $_.Name) -Recurse -Force
    }

if (-not $SkipNpmInstall) {
    Invoke-CheckedNative "npm" @("install", "--prefix", $toolboxUiBuildRoot)
}
Invoke-CheckedNative "npm" @("run", "build", "--prefix", $toolboxUiBuildRoot)

$inputHtml = Join-Path $toolboxUiBuildRoot "dist\index.html"
$outputFile = Join-Path $root "src\HUnityAutoTranslator.Toolbox\Web\ToolboxHtml.cs"
Write-ToolboxHtml -InputHtml $inputHtml -OutputFile $outputFile

if (Test-Path -LiteralPath $publishRoot) {
    Remove-Item -LiteralPath $publishRoot -Recurse -Force
}
if (Test-Path -LiteralPath $outputZip) {
    Remove-Item -LiteralPath $outputZip -Force
}

Invoke-CheckedNative "dotnet" @(
    "publish",
    $toolboxProject,
    "-c", $Configuration,
    "-r", $RuntimeIdentifier,
    "--self-contained", "true",
    "-o", $publishRoot,
    "-p:PublishSingleFile=true",
    "-p:Version=$PackageVersion",
    "-p:PackageVersion=$PackageVersion",
    "-p:FileVersion=$PackageVersion",
    "-p:InformationalVersion=$PackageVersion")

if (-not (Test-Path -LiteralPath (Join-Path $publishRoot "HUnityAutoTranslator.Toolbox.exe"))) {
    throw "Toolbox publish did not produce HUnityAutoTranslator.Toolbox.exe."
}

Compress-Archive -Path (Join-Path $publishRoot "*") -DestinationPath $outputZip -Force
Write-Host "Toolbox package directory: $publishRoot"
Write-Host "Toolbox package zip: $outputZip"
